
public class typecasting {
    public static  void main(String[]args){
        System.out.println("widening=========");
        double a=23;
        System.out.println(a);
        double b=(double) 25;
        System.out.println(b);
        System.out.println("narrowing====");
        int c=(int)23.43;
        System.out.println(c);


    }
}
